
def add(a,b):
    result = a + b
    return result


# additional 4 functions

def subtract(a,b):
    result_of_subtraction = a - b
    return result_of_subtraction

def divide(a,b):
    result_of_division = a / b
    return result_of_division

def multiply(a,b):
    result_of_multiply = a * b
    return result_of_multiply

def remainder(a,b):
    result_of_remainder = a % b
    return result_of_remainder
